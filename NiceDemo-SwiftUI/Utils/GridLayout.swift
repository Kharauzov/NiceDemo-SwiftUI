import Foundation

/// The 8-Point Grid System
enum GridLayout {
    ///  8 pts
    static let regularSpace: CGFloat = 8
    ///  16 pts
    static let doubleRegularSpace = 2 * regularSpace
    ///  24 pts
    static let trippleRegularSpace = 3 * regularSpace
    ///  32 pts
    static let fourthRegularSpace = 4 * regularSpace
    ///  20 pts
    static let commonSpace: CGFloat = 20
}

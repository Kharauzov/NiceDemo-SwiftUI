extension Color {
    struct AppColors {
        static let primary = Color( #colorLiteral(red: 0.2431372549, green: 0.3607843137, blue: 0.5019607843, alpha: 1) )
        static let white = Color( #colorLiteral(red: 0.9999999404, green: 1, blue: 1, alpha: 1) )
        static let black = Color( #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1) )
    }
}
